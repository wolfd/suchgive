        <h2>about</h2>

        <p>
            suchgive.org is a website dedicated to supporting the Dogecoin community’s tradition of collective selflessness and generosity. We believe these principles can be demonstrated through competitive charitable giving.
        </p>
        <p>
            Two charities compete in a timed “charity battle” - visitors can donate to either of the competing charities. Half of every donation is added to a reward pool. When the battle concludes, the winning charity receives the entire reward pool in addition to their donations.
        </p>
        <p>
            All donation funds will be exchanged to USD and sent to the charity participants at the conclusion of each battle. A complete record of previous donation payouts can be found on the records page.
        </p>
